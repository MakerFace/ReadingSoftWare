public class GoodWordBean {
    private String essayContent;
    private String essayNo;
    private String essayTitle;
    private String essayWritter;
    private String essayImage;

    public String getEssayImage() {
        return essayImage;
    }

    public void setEssayImage(String essayImage) {
        this.essayImage = essayImage;
    }


    public String getEssayContent() {
        return essayContent;
    }

    public void setEssayContent(String essayContent) {
        this.essayContent = essayContent;
    }

    public String getEssayNo() {
        return essayNo;
    }

    public void setEssayNo(String essayNo) {
        this.essayNo = essayNo;
    }

    public String getEssayTitle() {
        return essayTitle;
    }

    public void setEssayTitle(String essayTitle) {
        this.essayTitle = essayTitle;
    }

    public String getEssayWritter() {
        return essayWritter;
    }

    public void setEssayWritter(String essayWritter) {
        this.essayWritter = essayWritter;
    }
}
